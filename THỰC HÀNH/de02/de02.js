function show_model_tu(){
    var btn = document.getElementById("btn-tragop");
    var model = document.getElementById("model-tu");
  
    if(model.style.display == "block"){
      model.style.display = "none";
    }
    else 
    {
      model.style.display = "block";
    }
  }
  
